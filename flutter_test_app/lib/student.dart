import 'package:firestorm/annotation/firestorm_object.dart';
import 'package:flutter_test_app/person.dart';

@FirestormObject()
class Student extends Person {

  String studentID;
  String school;

  Student(super.id, super.firstname, super.lastname, super.age, super.height, super.isEmployed, super.friends, this.studentID, this.school);

}