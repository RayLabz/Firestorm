import 'package:flutter_test/flutter_test.dart';
import 'package:analyzer/dart/element/element.dart';
import 'package:analyzer/dart/element/nullability_suffix.dart';
import 'package:firestorm/exceptions/invalid_class_exception.dart';
import 'package:firestorm/gen/converter_generator.dart';
import 'package:firestorm/gen/extension_generator.dart';
import 'package:firestorm/gen/header_generator.dart';
import 'package:firestorm/gen/valid_class_holder.dart';

import '../support/fake_elements.dart';

void main() {
  group('HeaderGenerator', () {
    test('emits generated-file warning header', () {
      final buffer = StringBuffer();
      final output = HeaderGenerator.generateHeader(buffer);

      expect(output, contains('THE FOLLOWING FILE IS GENERATED BY FIRESTORM'));
      expect(output, contains('DO NOT MODIFY BY HAND'));
      expect(output, contains('Developed by RayLabz'));
    });
  });

  group('ExtensionGenerator', () {
    late TestClassElement userClass;
    late TestClassElement invalidClass;

    setUp(() {
      final coreLib = TestLibraryElement(isDartCore: true);
      final customLib = TestLibraryElement(isDartCore: false);

      final stringType = TestDartType(
        displayString: 'String',
        isDartCoreString: true,
        element: TestElement(displayName: 'String', library: coreLib),
      );
      final doubleType = TestDartType(
        displayString: 'double',
        isDartCoreDouble: true,
        element: TestElement(displayName: 'double', library: coreLib),
      );
      final intType = TestDartType(
        displayString: 'int',
        isDartCoreInt: true,
        element: TestElement(displayName: 'int', library: coreLib),
      );
      final boolType = TestDartType(
        displayString: 'bool',
        isDartCoreBool: true,
        element: TestElement(displayName: 'bool', library: coreLib),
      );

      final statusEnum = TestEnumElement(name: 'Status', displayName: 'Status', library: customLib);
      final statusType = TestInterfaceType(displayString: 'Status', element: statusEnum);

      final addressClass = TestClassElement(
        name: 'Address',
        displayName: 'Address',
        library: customLib,
        fields: <FieldElement>[
          TestFieldElement(name: 'id', type: stringType),
          TestFieldElement(name: 'street', type: stringType),
        ],
        unnamedConstructor: TestConstructorElement(
          parameters: <ParameterElement>[
            TestParameterElement(name: 'id', displayName: 'id', type: stringType, isPositional: true),
            TestParameterElement(name: 'street', displayName: 'street', type: stringType, isPositional: true),
          ],
        ),
      );
      final addressType = TestInterfaceType(displayString: 'Address', element: addressClass);

      final tokenType = TestDartType(
        displayString: 'String?',
        isDartCoreString: true,
        nullabilitySuffix: NullabilitySuffix.question,
        element: TestElement(displayName: 'String', library: coreLib),
      );

      final listOfIntType = TestInterfaceType(
        displayString: 'List<int>',
        element: TestClassElement(name: 'List', displayName: 'List', library: coreLib),
        isDartCoreList: true,
        typeArguments: <TestDartType>[intType],
      );

      final mapType = TestInterfaceType(
        displayString: 'Map<String, bool>',
        element: TestClassElement(name: 'Map', displayName: 'Map', library: coreLib),
        isDartCoreMap: true,
        typeArguments: <TestDartType>[stringType, boolType],
      );

      final userFields = <FieldElement>[
        TestFieldElement(name: 'id', type: stringType),
        TestFieldElement(name: 'score', type: doubleType),
        TestFieldElement(name: 'status', type: statusType),
        TestFieldElement(name: 'address', type: addressType),
        TestFieldElement(name: 'token', type: tokenType, metadata: <ElementAnnotation>[excludeAnnotation()]),
        TestFieldElement(name: 'tags', type: listOfIntType),
        TestFieldElement(name: 'flags', type: mapType),
      ];

      userClass = TestClassElement(
        name: 'User',
        displayName: 'User',
        library: customLib,
        fields: userFields,
        fieldLookup: <String, FieldElement>{for (final f in userFields) f.name: f},
        unnamedConstructor: TestConstructorElement(
          parameters: <ParameterElement>[
            TestParameterElement(name: 'id', displayName: 'id', type: stringType, isPositional: true),
            TestParameterElement(name: 'score', displayName: 'score', type: doubleType, isNamed: true),
            TestParameterElement(name: 'status', displayName: 'status', type: statusType, isNamed: true),
            TestParameterElement(name: 'address', displayName: 'address', type: addressType, isNamed: true),
            TestParameterElement(name: 'token', displayName: 'token', type: tokenType, isNamed: true),
            TestParameterElement(name: 'tags', displayName: 'tags', type: listOfIntType, isNamed: true),
            TestParameterElement(name: 'flags', displayName: 'flags', type: mapType, isNamed: true),
          ],
        ),
      );

      final secretFieldType = TestDartType(
        displayString: 'String',
        isDartCoreString: true,
        nullabilitySuffix: NullabilitySuffix.none,
        element: TestElement(displayName: 'String', library: coreLib),
      );

      final invalidFields = <FieldElement>[
        TestFieldElement(name: 'id', type: stringType),
        TestFieldElement(
          name: 'secret',
          type: secretFieldType,
          metadata: <ElementAnnotation>[excludeAnnotation()],
        ),
      ];

      invalidClass = TestClassElement(
        name: 'InvalidEntity',
        displayName: 'InvalidEntity',
        library: customLib,
        fields: invalidFields,
        fieldLookup: <String, FieldElement>{for (final f in invalidFields) f.name: f},
        unnamedConstructor: TestConstructorElement(
          parameters: <ParameterElement>[
            TestParameterElement(name: 'id', displayName: 'id', type: stringType, isPositional: true),
            TestParameterElement(name: 'secret', displayName: 'secret', type: secretFieldType, isPositional: true),
          ],
        ),
      );
    });

    test('generates toMap/fromMap snippets for nested objects, enums, lists, maps and doubles', () {

      final output = ExtensionGenerator.generateExtension(
        StringBuffer(),
        userClass,
        true,
        true,
      );

      expect(output, contains('extension UserModel on User'));
      expect(output, contains('static const bool fsSupport = true;'));
      expect(output, contains("'address': address.toMap(),"));
      expect(output, contains("'status': status.toString(),"));
      expect(output, isNot(contains("'token':")));
      expect(output, contains('(map[\'score\'] as num).toDouble()'));
      expect(output, contains('AddressModel.fromMap'));
      expect(output, contains('.cast<int>()'));
      expect(output, contains('.cast<String, bool>()'));
    });

    test('throws when @Exclude is used on non-nullable field', () {
      expect(
        () => ExtensionGenerator.generateExtension(
          StringBuffer(),
          invalidClass,
          true,
          true,
        ),
        throwsA(isA<InvalidClassException>()),
      );
    });
  });

  group('RegistryGenerator', () {
    test('generates registries and class registration calls', () {
      final one = TestClassElement(name: 'One', displayName: 'One');
      final two = TestClassElement(name: 'Two', displayName: 'Two');
      final holder = ValidClassHolder(
        <ClassElement>{one},
        <ClassElement>{one, two},
      );

      final output = RegistryGenerator.generateConverterFunctions(
        StringBuffer(),
        holder,
      );

      expect(output, contains('final Map<Type, Map<String, dynamic> Function(dynamic)> toMapRegistry'));
      expect(output, contains('convertToMap(dynamic object)'));
      expect(output, contains('convertFromMap<T>(Map<String, dynamic> map)'));
      expect(output, contains('FS.registerSerializer<One>'));
      expect(output, contains('RDB.registerSerializer<Two>'));
      expect(output, contains('LS.registerSerializer<One>'));
    });
  });
}




