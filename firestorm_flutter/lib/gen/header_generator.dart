class HeaderGenerator {

  static String generateHeader(StringBuffer headerBuffer) {
    headerBuffer.writeln('/* * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * */');
    headerBuffer.writeln('/* THE FOLLOWING FILE IS GENERATED BY FIRESTORM.               */');
    headerBuffer.writeln('/* DO NOT MODIFY BY HAND.                                      */');
    headerBuffer.writeln('/*                                                             */');
    headerBuffer.writeln('/* Generated on: ${DateTime.now().toIso8601String()}           */');
    headerBuffer.writeln("/* Firestorm for Flutter: http://github.com/raylabz/firestorm  */");
    headerBuffer.writeln("/* Developed by RayLabz.                                       */");
    headerBuffer.writeln('/* * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * */');
    headerBuffer.writeln();
    return headerBuffer.toString();
  }

}